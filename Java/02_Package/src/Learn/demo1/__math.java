package Learn.demo1;

public class __math {
    public static void main(String[] args) {
        System.out.println(Math.floor(30.11));  // 向下取整
        System.out.println(Math.ceil(-100.45));  // 向上取整
        System.out.println(Math.abs(-100));  // 绝对值
        System.out.println(Math.round(-100.45));  // 四舍五入

    }
}
